package com.sdp.mc.t0.web.bean;

public class T0ServiceBean {

	public String merchantNo;
	public String posMerchantNo;
	public String txnIds;

	public String getMerchantNo() {
		return merchantNo;
	}

	public void setMerchantNo(String merchantNo) {
		this.merchantNo = merchantNo;
	}

	public String getPosMerchantNo() {
		return posMerchantNo;
	}

	public void setPosMerchantNo(String posMerchantNo) {
		this.posMerchantNo = posMerchantNo;
	}

	public String getTxnIds() {
		return txnIds;
	}

	public void setTxnIds(String txnIds) {
		this.txnIds = txnIds;
	}

}
